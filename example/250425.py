print("print(\"Hello\\nWorld\")")

# ' 또는 ", \를 출력
# -> ' 또는 ", \ 앞에 \를 붙여주면 정상출력

# \를 붙이는 이유
# 이스케이프 코드를 통해 ' 또는 ", \를 출력할 수 있음
# 이스케이프 코드 : 미리 정해둔 '문자 조합'
# -> 기존의 문자가 가진 의미를 벗어나 부가적인 기능을 사용할 때 쓰이는 문자

# 자주 쓰는 이스케이프 코드 종류
# \n : 줄 바꿈
# \t : 탭 간격
# \\ : 문자 \를 그대로 출력
# \' : '를 그대로 출력
# \" : "를 그대로 출력